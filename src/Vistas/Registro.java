    private void Registrar() throws SQLException, ClassNotFoundException, UnsupportedEncodingException {
        String usuario = this.jTextField1.getText();
        String contrasena = this.jPasswordField1.getText();
        if (!usuario.equals("") && !contrasena.equals("")) {
            GestionUsuarios gestor = new GestionUsuarios();
            if (gestor.Registrar(usuario, contrasena)) {
                this.setVisible(false);
                new Principal();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor llena los campos.", "Alert", JOptionPane.WARNING_MESSAGE);
        }
    }

    @Override
    public void actionPerformed(ActionEvent ae) {
        if (ae.getSource() == this.jButton1) {
            try {
                this.Registrar();

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "No se pudo realizar la conexion con la base de datos.", "Alert", JOptionPane.WARNING_MESSAGE);
                System.out.print(ex);
            } catch (ClassNotFoundException ex) {
            } catch (UnsupportedEncodingException ex) {
            }
        }
        if (ae.getSource() == this.jButton2) {
            this.setVisible(false);
            new Principal();
        }
    }
